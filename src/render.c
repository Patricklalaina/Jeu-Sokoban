void	put_to_screen(int map[24][28], t_data *game, t_img wall, t_img collect, t_img object, t_img player)
{
	int	i;
	int	j;

	i = -1;
	while (++i != 24)
	{
		j = -1;
		while (++j != 28)
		{
			if (map[i][j] == WALL)
			{
				wall.pos.x = j * 25;
				wall.pos.y = i * 25;
				SDL_RenderCopy(game->render, wall.image, NULL, &wall.pos);
			}
			else if (map[i][j] == COLLECT)
			{
				collect.pos.x = j * 25;
				collect.pos.y = i * 25;
				SDL_RenderCopy(game->render, collect.image, NULL, &collect.pos);
			}
			else if (map[i][j] == CAISSE || map[i][j] == CAISSE_CO)
			{
				object.pos.x = j * 25;
				object.pos.y = i * 25;
				SDL_RenderCopy(game->render, object.image, NULL, &object.pos);
			}
			else if (map[i][j] == PLAYER || map[i][j] == PLAYER_CO)
			{
				player.pos.x = j * 25;
				player.pos.y = i * 25;
				SDL_RenderCopy(game->render, player.image, NULL, &player.pos);
			}
		}
	}
}

char	*get_str_nb(int i)
{
	if (i == 0)
		return ("00");
	else if (i == 1)
		return ("01");
	else if (i == 2)
		return ("02");
	else if (i == 3)
		return ("03");
	else if (i == 4)
		return ("04");
	else if (i == 5)
		return ("05");
	else if (i == 6)
		return ("06");
	return (NULL);
}

